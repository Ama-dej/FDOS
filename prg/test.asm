[BITS 16]
[ORG 0x0000]

	MOV AH, 0x11
	MOV BX, BUFFER
	MOV BX, 0
	MOV SI, FILENAME
	MOV CX, 0xFFFF
	MOV DX, 256
	MOV DI, 0
	INT 0x80

	MOV AH, 0x20
	MOV DI, BUFFER
	INT 0x80

	MOV AH, 0x01
	MOV SI, BUFFER
	MOV CX, 80
	INT 0x80

	CMP AL, 0x03
	JNE EXIT

	MOV AH, 0x01
	MOV SI, SWER
	MOV CX, 3
	INT 0x80

EXIT:
	MOV AH, 0x00
	INT 0x80

FILENAME: DB "ABC.BIN", 0x00
SWER: DB "fak"
BUFFER:
	MOV AH, 0x0E
	MOV AL, 'A'
	INT 0x10

	MOV AH, 0x00
	INT 0x80

	TIMES 1024 - ($ - $$) DB 'G'
BUFFER_END:
