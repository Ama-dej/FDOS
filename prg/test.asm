	MOV AH, 0x0E
	MOV AL, 'd'
	INT 0x10

	JMP (LABEL - BUFFER)

TIMES 1024 * 3 DB 0
	
LABEL:
	MOV AL, 'E'
	INT 0x10

	MOV AH, 0x00
